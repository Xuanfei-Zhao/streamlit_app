# 行业重分类模型方法
st.markdown("## 行业重分类模型方法")
st.markdown("""
本项目构建了一套全流程自动化行业分类体系，核心逻辑为：基于A股上市公司多年年报主营业务文本，通过无监督文本挖掘、网络社群发现和层级递归拆分，重新识别企业业务相似关系，并生成可解释的三级行业分类。
""")

st.markdown("### 一、主营业务文本精准提取")
st.markdown("""
首先对年报TXT文件进行多编码兼容读取，并根据报告标题、章节关键词定位业务相关章节；在章内进一步执行句子级业务相关性评分，包含强业务词的句子加权保留，含有噪声词的句子则被降低权重或剔除。
""")

st.markdown("### 二、文本向量化与相似度度量")
st.markdown("""
对清洗后的企业文本进行TF-IDF 向量化，能够突出企业业务文本中具有区分度的关键词，降低泛化词的影响。随后，机器人使用 Truncated SVD 对高维文本向量进行降维，形成每家公司对应的业务语义向量。 

在得到企业文本向量后，机器人通过余弦相似度计算任意两家公司之间的主营业务相似程度。余弦相似度越高，说明两家企业在主营业务描述、产品结构、应用领域或产业链位置上越接近。
""")

st.markdown("### 三、构建稀疏化的TNIC式企业相似度网络")
st.markdown("""
为避免稠密全连接网络引入过多弱关系，机器人借鉴TNIC 的思路，构建稀疏企业相似度网络，方法采用mutual top-k 策略建边：仅当企业 A 处于企业 B 的 Top-K 近邻内，且企业 B 也处于企业 A 的 Top-K 近邻内，同时两者的余弦相似度不低于预设阈值，才在网络中建立一条强同行关系边。

经过多组参数的扫描与比较，最终确定主模型网络为mutual top-25、相似度阈值 0.50。该网络要求两家企业必须互相将对方视为最相似的 25 家企业之一，且文本相似度不低于 0.5，从而形成稀疏而高信度的企业相似度图。
""")

st.markdown("### 四、层级递归Leiden 社群发现")
st.markdown("""
在新构建的企业相似度网络上，采用Leiden 算法进行层级社群发现。

- **一级行业生成**：在全网以低分辨率进行扫描，综合核心行业数量、最大行业占比、小簇比例、组内相似度和随机种子稳定性等指标，确定一级行业划分，最终得到29个一级行业。

- **二级行业拆分**：在每个一级行业内部继续构建子网络，并再次运行Leiden 算法，识别二级行业，用于刻画一级行业内部的细分方向。

- **三级行业细分**：在每个二级行业内部继续递归拆分，生成三级行业。三级行业对应更具体的业务领域，是机器人行业重分类结果中最能体现细分识别能力的层级。

通过这种父节点内逐层拆分的方式，保证三级行业严格嵌套于二级行业，二级行业严格嵌套于一级行业，形成29个一级、114个二级、267个三级行业的层级架构。
""")

st.markdown("### 五、小簇、孤立点与边界企业回填")
st.markdown("""
针对聚类后产生的小规模社群和未形成稳定群组的孤立企业，方法综合三类信号进行智能回填：企业与候选行业Top-5 企业的平均相似度、与候选行业质心的相似度、以及其近邻中属于候选行业的比例。
""")

st.markdown("### 六、基于大模型的行业标签生成")
st.markdown("""
调用通义千问API，输入社群的关键词、代表企业、主营业务摘要、父级行业标签等结构化信息，输出建议标签、短标签、标准化标签、命名理由、行业边界说明、是否混合行业、是否跨行业、标签置信度等。随后通过规则对标签进行后处理，检测标签过泛、过长、父子粒度重复、并列拼接过多等情况，并与申万分布交叉验证，确保标签字典的规范与自洽。
""")

st.markdown("### 七、跨行业公司识别与软隶属确定")
st.markdown("""
通过文本语义、近邻分布和向量空间距离等多个维度，构建企业"业务多元化"的量化指标，然后对判定为多元化的企业，基于语义网络和近邻投票等方式，给出其可能涉足的多个行业（一级、二级、三级）及对应的隶属强度。（多元化公司可能属于多个行业）
""")

st.markdown("### 八、财务指标分析")
st.markdown("""
预设六个评价维度（盈利能力、流动性、创现能力、股东分化、偿债能力、资产使用效率）。先对A股上市公司的财务数据进行极端值、缺失值的处理以及归一化处理。采用CRITIC的方法，分行业与年份，动态、客观地确定每个维度内各指标的权重。找到其分位数数据。对每个维度，根据该（年份，行业，维度）下预计算的权重，对该公司在该维度内存在的有效指标进行加权平均（若权重和为0 则退化为等权平均），得到维度得分。
""")

